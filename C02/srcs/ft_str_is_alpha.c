#include "../includes/libft.h"

int		ft_str_is_alpha(char *str)
{
	while (*(str))
	{
		if ((*(str) >= 'A' && *(str) <= 'Z') ||
			(*(str) >= 'a' && *(str) <= 'z'))
		{
			str++;
		}
		else
			return (0);
	}
	return (1);
}
